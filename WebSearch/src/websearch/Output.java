    public void autoExecute() throws FileNotFoundException, IOException{
        txtOutput.setText("");
        
            File file = new File("D:\\log.txt");

            FileReader arq = new FileReader(file);
            BufferedReader lerArq = new BufferedReader(arq);
            
            String linha = lerArq.readLine();
            while (linha != null){
                txtOutput.setText(txtOutput.getText()+System.getProperty( "line.separator" )+linha);
                linha = lerArq.readLine();
            }
         arq.close();
 
    }
    
